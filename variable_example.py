# Let's say you have a box to store things.
# You can give the box a name, like "my_box".
# Here, "my_box" is a variable

my_box = "toys"
print (my_box)  # Output : toys

# You store numbers in a variable

my_number = 10
print (my_number)

# You can also combine variable together
 
my_message = "Hello" + "\t" + "World!"
print (my_message)